    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VeiculoDAO dao = new VeiculoDAO();

    if (veiculoEditando != null) {
        // Modo edição
        veiculoEditando.setPlaca(jTextField1.getText());
        veiculoEditando.setMarca(jTextField2.getText());
        veiculoEditando.setModelo(jTextField3.getText());
        veiculoEditando.setCor(jTextField4.getText());
        veiculoEditando.setCpfCliente(jTextField5.getText());

        dao.atualizarVeiculo(placaOriginal, veiculoEditando);
        JOptionPane.showMessageDialog(this, "Veículo atualizado com sucesso!");
    } else {
        // Modo cadastro novo
        Veiculo veiculo = new Veiculo();
        veiculo.setPlaca(jTextField1.getText());
        veiculo.setMarca(jTextField2.getText());
        veiculo.setModelo(jTextField3.getText());
        veiculo.setCor(jTextField4.getText());
        veiculo.setCpfCliente(jTextField5.getText());

        dao.cadastrarVeiculo(veiculo);
        JOptionPane.showMessageDialog(this, "Veículo cadastrado com sucesso!");
    }

    this.dispose(); // fecha a tela
    }//GEN-LAST:event_jButton1ActionPerformed

