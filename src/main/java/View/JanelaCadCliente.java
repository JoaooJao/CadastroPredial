    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ClienteDAO dao = new ClienteDAO();

        if (clienteEditando != null) {
            // Modo edição → fazer update
            clienteEditando.setNome(jTextField1.getText());
            clienteEditando.setCpf(jTextField2.getText());
            clienteEditando.setTelefone(jTextField3.getText());
            clienteEditando.setCidade(jTextField4.getText());

            dao.atualizarCliente(cpfOriginal, clienteEditando);

            JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!");
        } else {
            // Modo cadastro novo
            Cliente cliente = new Cliente();
            cliente.setCpf(jTextField2.getText());
            cliente.setNome(jTextField1.getText());
            cliente.setTelefone(jTextField3.getText());
            cliente.setCidade(jTextField4.getText());

            dao.cadastrarCliente(cliente);

            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
        }

        this.dispose(); // fecha a tela
    }//GEN-LAST:event_jButton1ActionPerformed

